
public class Reversepattern {

	public static void main(String[] args) {
		int i,j;
	for(i=5;i>=1;i--){
		for(j=1;j<=i;j++) 
		{
			System.out.print(i+" ");  
		}
		System.out.println();
		
	}	
//	
//Above progrm is to print following pattern
//5 5 5 5 5 
//4 4 4 4 
//3 3 3 
//2 2 
//1 
	//spaces
	System.out.println();
	//spaces
	for(i=5;i>=1;i--){
		for(j=1;j<=i;j++) 
		{
			System.out.print(j+" ");
		}
		System.out.println();
		}
	}}
